var ObjectID = require('mongodb').ObjectID;
var mongodb = require('./mongodb.js');
var logger = require('../../logger.js');

/** factory that returns a generic crud implementations with mongo */
module.exports.crud = function (colname, sort, limit, hasObjectId) {
    logger.debug("crud para " + colname);
    /** object for perform basic crud actions in a collection */
    return {
        _colName: colname,
        _sort: sort || {
            _id: -1
        },
        _limit: limit || 100,
        _oID: hasObjectId || true,
        finding: finding,
        inserting: inserting,
        updating: updating,
        removing: removing,
        counting: counting
    };
}

function finding(mongoQ) {
    return mongodb.finding(this._colName, forceId(mongoQ.query, this._oID), null, mongoQ.skip, mongoQ.limit || this._limit, mongoQ.sort || this._sort);
}

function inserting(document) {
    return mongodb.inserting(this._colName, document);
}

function updating(id, document) {
    return mongodb.updating(this._colName, forceId({ _id: id }, this._oID), forceId(document, this._oID), null);
}

function removing(id, document) {
    return mongodb.removing(this._colName, forceId({ _id: id }, this._oID), null);
}

function counting() {
    return mongodb.counting(this._colName);
}


/** the autogenerated _id properties are custom mongo objects */
function forceId(document, hasObjectID) {
    if (document._id && hasObjectID) {
        document._id = new ObjectID(document._id);
    }
    return document;
}